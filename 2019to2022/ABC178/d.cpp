const int MAX = 510000;
const int MOD = 1000000007;

long long fac[MAX], finv[MAX], inv[MAX];

// テーブルを作る前処理
void COMinit() {
    fac[0] = fac[1] = 1;
    finv[0] = finv[1] = 1;
    inv[1] = 1;
    for (int i = 2; i < MAX; i++){
        fac[i] = fac[i - 1] * i % MOD;
        inv[i] = MOD - inv[MOD%i] * (MOD / i) % MOD;
        finv[i] = finv[i - 1] * inv[i] % MOD;
    }
}

// 二項係数計算
long long COM(int n, int k){
    if (n < k) return 0;
    if (n < 0 || k < 0) return 0;
    return fac[n] * (finv[k] * finv[n - k] % MOD) % MOD;
}

int main() {
    ll n;
    cin >> n;
    if (n<3){
        cout << 0 << endl;
        return 0;
    }
    COMinit();
    ll ans = 0;
    ll sho = n / 3;
    ll res = n % 3;
    while (sho>0){
        ans += COM(res+sho-1, res);
        sho--;
        res += 3;
        ans %= MOD;
    }
    cout << ans << endl;
    //printf("%.12f", ans);
}