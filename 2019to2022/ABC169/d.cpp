vector<pair<long long, long long> > prime_factorize(long long N) {
    vector<pair<long long, long long> > res;
    for (long long a = 2; a * a <= N; ++a) {
        if (N % a != 0) continue;
        long long ex = 0; // 指数

        // 割れる限り割り続ける
        while (N % a == 0) {
            ++ex;
            N /= a;
        }

        // その結果を push
        res.push_back({a, ex});
    }

    // 最後に残った数について
    if (N != 1) res.push_back({N, 1});
    return res;
}

int main() {
    ll N;
    cin >> N;
    const auto &res = prime_factorize(N); //(素因数, 数)
    ll ans = 0;
    for (auto p: res){
        ll count=2;
        ll residue = p.second - 1;
        while (residue>=0){
            ++ans;
            residue -= count;
            ++count;
        }
    }
    cout << ans << endl;
}